ALTER TABLE templates ENABLE ROW LEVEL SECURITY;
ALTER TABLE form_configs ENABLE ROW LEVEL SECURITY;
ALTER TABLE generated_documents ENABLE ROW LEVEL SECURITY;

-- Allow anyone to read templates
CREATE POLICY "Anyone can read templates" ON templates
  FOR SELECT TO anon, authenticated
  USING (true);

-- Allow authenticated users to create/update templates
CREATE POLICY "Authenticated users can manage templates" ON templates
  FOR ALL TO authenticated
  USING (true)
  WITH CHECK (true);

-- Similar policies for form_configs and generated_documents
CREATE POLICY "Anyone can read form_configs" ON form_configs
  FOR SELECT TO anon, authenticated
  USING (true);

CREATE POLICY "Authenticated users can manage form_configs" ON form_configs
  FOR ALL TO authenticated
  USING (true)
  WITH CHECK (true);

CREATE POLICY "Anyone can read generated_documents" ON generated_documents
  FOR SELECT TO anon, authenticated
  USING (true);

CREATE POLICY "Authenticated users can manage generated_documents" ON generated_documents
  FOR ALL TO authenticated
  USING (true)
  WITH CHECK (true);
